export type SmilyEntry = { [code: string]: string }

export type SmilyMap = { [codePrefix: string]: SmilyEntry }

export const kDefaultPrefix = 's'

function fillSmilyMap(
  entry: SmilyEntry,
  kind: SmilyKindItem,
  item: SmilyItem,
  offset?: number,
  customCode?: number
) {
  let code = item.code ?? item.id
  if (customCode !== undefined) {
    code = customCode
  } else if (offset !== undefined) {
    code = (code as number) + offset
  }
  entry[code.toString()] = `${kind.path}/${item.path}`
}

export const smilyMaps: SmilyMap = {}
smilyData.forEach((kind) => {
  const codePrefix = kind.codePrefix ?? kDefaultPrefix
  if (!smilyMaps[codePrefix]) {
    smilyMaps[codePrefix] = {}
  }
  kind.items.forEach((item) => fillSmilyMap(smilyMaps[codePrefix], kind, item))
})

